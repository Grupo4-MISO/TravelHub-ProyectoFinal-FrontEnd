ET.register_namespace('', NS)

OLD_FILE = 'src/locale/messages.en.xlf'
NEW_FILE = 'src/locale/messages.xlf'
OUT_FILE = 'src/locale/messages.en.xlf'

def parse_units(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        text = f.read()
    units = []
    pattern = re.compile(r'<trans-unit[^>]*id="([^"]*)"[^>]*>(.*?)</trans-unit>', re.DOTALL)
    for m in pattern.finditer(text):
        uid = m.group(1)
        inner = m.group(2)
        sm = re.search(r'(<source>.*?</source>)', inner, re.DOTALL)
        source_xml = sm.group(1) if sm else ''
        units.append((uid, source_xml, m.group(0)))
    return units

def source_to_target_map(units):
    mapping = {}
    for uid, sxml, full in units:
        tm = re.search(r'(<target>.*?</target>)', full, re.DOTALL)
        if tm:
            mapping[sxml] = tm.group(1)
    return mapping

